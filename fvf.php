  <h1>Nuclear fission vs Nuclear fusion</h1>
  <h2>Similarities</h2>
  <p>Fission and fusion are both atomic processes that release large amounts of energy, but they are basically opposites. Fission is the process of splitting an atom's nucleus, usually a heavier atom. Fusion is combining 2 or more lighter atoms.</p>
  <h2>What is nuclear fission?</h2>
  <p>Nuclear Fission happens when heavy elements like uranium suddenly and spontaneously decay. This causes their nuclei to split in half. This process releases a large ammount of energy and heat to be collected by the power plant. Nuclear fission is what happens in a steriotypical power plant. "Physicists Lise Meitner and Otto Frisch discovered the principles underlying fission after receiving a private letter from nuclear chemist Otto Hahn in December 1938." (<a href="https://www.livescience.com/fission-vs-fusion.html">livescience.com</a>). Hahn's experiments showed that when uranium atoms were showered with neutrons, they would split. They realized the terrible implications of their discovery, which was happening during World War II. Many fission reactions happening at the same time had the potential to create something similar to an atomic bomb.</p>
  <h2>What is nuclear fusion?</h2>
  <p>Nuclear Fusion has yet to be fully developed as a sustainable power source. In nuclear fusion, two nuclei of a light element, such as hydrogen, are pushed against the magenetic forge and merge into a single, heavier nucleus. This creates lots of energy but takes a lot of force to push the 2 atoms together.<br>
<br>
Fun Fact: Nuclear fusion is what happens in the center of a star. The pressure from the outside forces nuclear fusion to happen and that creates a lot of Energy, Light, and Heat.<br>
<br>
Nuclear Fusion power would produce less nuclear waste than fission and uses common, cheaper, and lighter elements such as hydrogen rather than something rarer and more expensive like uranium as a fuel supply. Nuclear fusion reactors could be here as soon as 2025 but they aren't expected to generate more power than they consume until at least the 2030s.
  </p>
  <h2>Dangers</h2>
  <p>Both types of nuclear power similar issues. As we have seen with nuclear fussion plants, they can create a lot of radiation. An example of this was the Chernobyl disaster in Soviet Union. This happend in April 25–26, 1986. The power plant consisted of 4 reactors. This is considered to be the largest nuclear accident in history and for good reason.<br>
<br>
At reactor 4, the workers shut down the reactor’s power-regulating system and its emergency safety systems, and let the reactor to continue running at 7 percent power. These mistakes created a domino effect, and at 1:23 AM on April 26 the chain reaction in the core went out of control. "Several explosions triggered a large fireball and blew off the heavy steel and concrete lid of the reactor. This and the ensuing fire in the graphite reactor core released large amounts of radioactive material into the atmosphere, where it was carried great distances by air currents. A partial meltdown of the core also occurred." (Britanica). On April 27 the 30,000 inhabitants of the nearest town, Pryp’yat began to be evacuated. On the 18th, the Swedish monitoring stations reported that there were enormous ammounts of radioactive material being transported in the wind.<br>
<br>
There were from 2-50 deaths in the initial explosion but there were dozens of people that died of "radiation sickness". By now there were multiple times the ammount of "radionuclides" which are a radioactive form of some elements than were created by the atomic bombs dropped on Hiroshima and Nagasaki, Japan.</p>
